package JAVA_PRAC;

public class JAVA_Operators {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;
        int c = a+b;
        System.out.println(c);
        int d = a/b;
        System.out.println(d);
        d=a%b;
        System.out.println(d);
        d ++;
        System.out.println(d);
        d^=0;
        System.out.println(d);
    }
    
}
